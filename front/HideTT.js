function HideTT1()
{
  if(document.getElementsByClassName('tooltip_2')[0].style.visibility=='visible')
  {
    document.getElementsByClassName('tooltip_2')[0].style.visibility='hidden';
    document.getElementsByClassName('strelka_2')[0].style.visibility='hidden';
    document.getElementsByClassName('img_2')[0].style.visibility='hidden';
  }
}

